i_c = int(np.argmin(np.abs(h_sweep - 1.0)))
ax_a.plot(P_sweep[i_c, 0], P_sweep[i_c, 1], marker="*", color="black", ms=17,
          mec="white", mew=0.8, zorder=5, label=r"critical point $h_c=1$")
ax_a.scatter(P_train[spt_m, 0], P_train[spt_m, 1], s=70, color=C_SPT, marker="o",
             edgecolors="white", linewidths=0.7, zorder=3, label=r"train: SPT $h<1$")
