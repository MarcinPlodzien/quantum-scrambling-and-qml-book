OUTPUT_DIR = SCRIPT_DIR.parent / "figures" / "ch7"
DATA_DIR = SCRIPT_DIR / "data" / "ch7"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

I2 = np.eye(2, dtype=complex)
X = np.array([[0, 1], [1, 0]], dtype=complex)
Y = np.array([[0, -1j], [1j, 0]], dtype=complex)
Z = np.array([[1, 0], [0, -1]], dtype=complex)


def kron_op(single, site, N):
    op = np.array([[1.0]], dtype=complex)
    for j in range(N):
        op = np.kron(op, single if j == site else I2)
    return op


def kron_string(ops_by_site, N):
    """Full operator from a dict {site: 2x2 Pauli}, identity elsewhere."""
    op = np.array([[1.0]], dtype=complex)
    for j in range(N):
        op = np.kron(op, ops_by_site.get(j, I2))
    return op


def cluster_ising(N, J, h):
    """Cluster-Ising chain on an OPEN chain:
    H(h) = -J sum_i Z_{i-1} X_i Z_{i+1} - h sum_i X_i.
    SPT (cluster) phase for h<1, trivial paramagnet for h>1, transition at h/J=1."""
    H = np.zeros((2 ** N, 2 ** N), dtype=complex)
    for i in range(1, N - 1):
        H += -J * (kron_op(Z, i - 1, N) @ kron_op(X, i, N) @ kron_op(Z, i + 1, N))
    for i in range(N):
        H += -h * kron_op(X, i, N)
    return H


def sublattice_parity(N):
    """The two generators of the protecting Z2 x Z2 symmetry:
    P_even = prod_{i even} X_i, P_odd = prod_{i odd} X_i.  Each commutes with every
    cluster term Z_{i-1}X_iZ_{i+1} and with the transverse field, so it is a genuine
    symmetry of H(h)."""
    Pe = np.eye(2 ** N, dtype=complex)
    Po = np.eye(2 ** N, dtype=complex)
    for s in range(N):
        if s % 2 == 0:
            Pe = Pe @ kron_op(X, s, N)
        else:
            Po = Po @ kron_op(X, s, N)
    return Pe, Po


def symmetry_projector(N):
    """Projector onto the (P_even, P_odd) = (+1, +1) symmetry sector.  The open
    chain's edge modes make the cluster-phase ground state near-degenerate; fixing
    the sector selects one unique, symmetric ground state (the sector that also
    contains the trivial all-|+> state), so the string order is well defined."""
    Pe, Po = sublattice_parity(N)
    return ((np.eye(2 ** N) + Pe) / 2) @ ((np.eye(2 ** N) + Po) / 2)


def ground_state(N, J, h, Proj, lam=50.0):
    """Symmetric ground state of H(h): diagonalize H + lam (I - Proj), which lifts
    every other symmetry sector by lam and leaves the (+1,+1) sector untouched, so
    the lowest eigenvector is the unique symmetric ground state."""
    Hm = cluster_ising(N, J, h) + lam * (np.eye(2 ** N) - Proj)
    w, v = eigh(Hm)
    return v[:, 0].astype(complex)


def string_operator(N):
    """The cluster string order operator S = product of the bulk stabilizers
    K_i = Z_{i-1}X_iZ_{i+1}, which telescopes to
        Z_0 Y_1 X_2 X_3 ... X_{N-3} Y_{N-2} Z_{N-1}
    (Z on the two ends, Y just inside, X across the bulk).  <S> = 1 on the ideal
    cluster state and decays to 0 in the trivial phase."""
    ops = {0: Z, N - 1: Z, 1: Y, N - 2: Y}
    for j in range(2, N - 2):
        ops[j] = X
    return kron_string(ops, N)


def mixed_field_ising(N, Jr, hx, hz):
    """Nonintegrable mixed-field Ising reservoir
    H_res = Jr sum Z_iZ_{i+1} + hx sum X_i + hz sum Z_i.  The longitudinal hz breaks
    the integrability of the pure transverse model AND the cluster Z2 x Z2 symmetry,
    so U = exp(-i H_res t) both scrambles the state and rotates the symmetry-forbidden
    readout observables into directions that overlap the nonlocal string order."""
    H = np.zeros((2 ** N, 2 ** N), dtype=complex)
    for i in range(N - 1):
        H += Jr * (kron_op(Z, i, N) @ kron_op(Z, i + 1, N))
    for i in range(N):
        H += hx * kron_op(X, i, N) + hz * kron_op(Z, i, N)
    return H


_POP = np.array([bin(i).count("1") for i in range(1 << 16)])
_PH = np.array([1, 1j, -1, -1j])


def pauli_readout(N):
    """A FIXED bank of low-weight observables that are FORBIDDEN by the protecting
    Z2 x Z2 symmetry: the single-site magnetizations {Y_i, Z_i} and nearest-neighbour
    correlators {Y_iY_{i+1}, Z_iZ_{i+1}}.  These are exactly the conventional local
    order parameters, and because the SPT phase breaks no symmetry every one of them
    vanishes IDENTICALLY in both phases, so the bare readout carries no signal.  For a
    pure state psi a Pauli P = i^p X^x Z^z has <P> = Re sum_b conj(psi[b]) W[b]
    psi[b^x], an O(2^N) contraction (checked against brute-force <psi|P|psi>)."""
    bit = lambda i: 1 << (N - 1 - i)
    specs, names = [], []
    for i in range(N):
        specs += [(bit(i), bit(i), 1), (0, bit(i), 0)]              # Y_i, Z_i
        names += [f"Y{i}", f"Z{i}"]
    for i in range(N - 1):
        m = bit(i) | bit(i + 1)
        specs += [(m, m, 2), (0, m, 0)]                             # Y_iY_{i+1}, Z_iZ_{i+1}
        names += [f"Y{i}Y{i+1}", f"Z{i}Z{i+1}"]
    b = np.arange(1 << N)
    COLS = np.array([b ^ x for (x, z, p) in specs])
    W = np.array([_PH[p] * (1 - 2 * (_POP[z & b] & 1)) for (x, z, p) in specs],
                 dtype=complex)
    return COLS, W, names


def qelm_features(psi_list, U, COLS, W):
    """Feature matrix F (n_inputs x d).  For each ground state psi, either read the
    bank on the bare state (U is None) or on the scrambled state phi = U psi,
    f_alpha = <phi|O_alpha|phi> = <psi|U^dag O_alpha U|psi>.  Nothing here is
    trained; U and the readout set are fixed once and for all."""
    F = np.zeros((len(psi_list), COLS.shape[0]))
    for n, psi in enumerate(psi_list):
        phi = U @ psi if U is not None else psi
        F[n] = np.real(np.conj(phi) * W * phi[COLS]).sum(axis=1)
    return F


def add_shot_noise(F, shots, rng):
    """Finite-shot estimate of each Pauli expectation: variance (1 - <O>^2)/shots.
    On the bare forbidden features (true value 0) this is pure noise, so a readout
    is at chance no matter how many shots are taken; on the scrambled features the
    O(0.1) signal survives easily."""
    var = np.clip(1.0 - F ** 2, 0.0, 1.0) / shots
    return F + rng.normal(size=F.shape) * np.sqrt(var)


# ---------------------------------------------------------------------------
# Model / protocol parameters.
N = 8                        # open cluster-Ising chain (2^N = 256 dim)
J = 1.0                      # cluster coupling; critical point at h_c = J = 1
JR, HX, HZ = 1.0, 1.05, 0.5  # nonintegrable, symmetry-breaking mixed-field reservoir
T_EV = 2.5                   # fixed scrambling time in U = exp(-i H_res t)
N_SHOTS = 1024               # finite measurement shots for the bare-vs-scrambled bars
N_REAL = 60                  # shot-noise realizations averaged for the ablation

# Training fields, drawn ONLY from the clearly-SPT and clearly-trivial regions.
H_TRAIN_SPT = np.linspace(0.05, 0.55, 10)   # cluster / SPT, label 0
H_TRAIN_TRIV = np.linspace(1.45, 2.50, 10)  # trivial paramagnet, label 1
# Independent held-out fields, same clear regions, for the reported accuracy.
H_TEST_SPT = np.linspace(0.10, 0.50, 8)
H_TEST_TRIV = np.linspace(1.50, 2.45, 8)
# Dense sweep across the transition (includes the critical region) for (a)-(c).
H_SWEEP = np.linspace(0.02, 2.5, 120)


def compute():
    from sklearn.linear_model import LogisticRegression
    from sklearn.preprocessing import StandardScaler
    from sklearn.decomposition import PCA

    COLS, W, names = pauli_readout(N)
    Proj = symmetry_projector(N)
    U = expm(-1j * mixed_field_ising(N, JR, HX, HZ) * T_EV)
    Sop = string_operator(N)
    mid = N // 2
    ZZ_loc = kron_op(Z, mid, N) @ kron_op(Z, mid + 1, N)   # a local order parameter

    def states(hs):
        return [ground_state(N, J, h, Proj) for h in hs]

    h_train = np.concatenate([H_TRAIN_SPT, H_TRAIN_TRIV])
    y_train = np.concatenate([np.zeros(len(H_TRAIN_SPT)), np.ones(len(H_TRAIN_TRIV))])
    h_test = np.concatenate([H_TEST_SPT, H_TEST_TRIV])
    y_test = np.concatenate([np.zeros(len(H_TEST_SPT)), np.ones(len(H_TEST_TRIV))])

    psi_train, psi_test, psi_sweep = states(h_train), states(h_test), states(H_SWEEP)

    # Scrambled features (exact expectations) for the main classifier and PCA.
    F_train = qelm_features(psi_train, U, COLS, W)
    F_test = qelm_features(psi_test, U, COLS, W)
    F_sweep = qelm_features(psi_sweep, U, COLS, W)
    # Bare features (no reservoir): forbidden by symmetry, so ~0 to machine precision.
    B_train = qelm_features(psi_train, None, COLS, W)
    B_test = qelm_features(psi_test, None, COLS, W)

    # Standardize on the scrambled training features, fit the LINEAR (logistic) readout.
    scaler = StandardScaler().fit(F_train)
    clf = LogisticRegression(C=1.0, max_iter=5000)
    clf.fit(scaler.transform(F_train), y_train)

    acc_test = float(clf.score(scaler.transform(F_test), y_test))
    acc_train = float(clf.score(scaler.transform(F_train), y_train))
    prob_sweep = clf.predict_proba(scaler.transform(F_sweep))[:, 1]   # P(trivial)

    # Decision crossing P = 1/2 by linear interpolation of the sweep.
    cr = np.where(np.diff(np.sign(prob_sweep - 0.5)) != 0)[0]
    if len(cr):
        k = cr[0]
        p0, p1 = prob_sweep[k], prob_sweep[k + 1]
        h_cross = float(H_SWEEP[k] + (0.5 - p0) / (p1 - p0) * (H_SWEEP[k + 1] - H_SWEEP[k]))
    else:
        h_cross = float("nan")

    # 2D PCA of the scrambled feature vectors: fit on training, project everything.
    pca = PCA(n_components=2).fit(scaler.transform(F_train))
    P_train = pca.transform(scaler.transform(F_train))
    P_sweep = pca.transform(scaler.transform(F_sweep))

    # Ablation: bare vs scrambled linear-readout accuracy under identical finite-shot
    # noise, averaged over realizations.  Bare is forced to chance because the
    # forbidden features are exactly zero; scrambled inherits the string order.
    def noisy_acc(Ftr, Fte):
        rng = np.random.default_rng(0)
        accs = []
        for _ in range(N_REAL):
            Ftr_n = add_shot_noise(Ftr, N_SHOTS, rng)
            Fte_n = add_shot_noise(Fte, N_SHOTS, rng)
            sc = StandardScaler().fit(Ftr_n)
            c = LogisticRegression(C=1.0, max_iter=5000).fit(sc.transform(Ftr_n), y_train)
            accs.append(c.score(sc.transform(Fte_n), y_test))
        return float(np.mean(accs)), float(np.std(accs))

    acc_bare, acc_bare_sd = noisy_acc(B_train, B_test)
    acc_scram, acc_scram_sd = noisy_acc(F_train, F_test)

    # Physics: string order and the local order parameter across the sweep.
    S_sweep = np.array([np.vdot(p, Sop @ p).real for p in psi_sweep])
    ZZ_sweep = np.array([np.vdot(p, ZZ_loc @ p).real for p in psi_sweep])
    bare_mag = float(np.abs(B_train).max())   # size of the bare features (~1e-15)

    # Phase-averaged string order (for the printed verification).
    S_spt = float(S_sweep[H_SWEEP < 0.9].mean())
    S_triv = float(S_sweep[H_SWEEP > 1.1].mean())

    print(f"  N={N}, reservoir (Jr,hx,hz)=({JR},{HX},{HZ}), t={T_EV}, "
          f"d={F_train.shape[1]} symmetry-forbidden features")
    print(f"  string order  S(SPT h<0.9) = {S_spt:+.3f}   S(trivial h>1.1) = {S_triv:+.3f}")
    print(f"  local <Z_iZ_i+1> range over sweep: "
          f"[{ZZ_sweep.min():+.1e}, {ZZ_sweep.max():+.1e}]  (flat ~0, symmetry-forbidden)")
    print(f"  bare feature magnitude |f|max = {bare_mag:.1e}  (zero to machine precision)")
    print(f"  ablation accuracy: BARE = {acc_bare:.3f} +/- {acc_bare_sd:.3f} (chance)  "
          f"SCRAMBLED = {acc_scram:.3f} +/- {acc_scram_sd:.3f}")
    print(f"  main (scrambled) held-out accuracy = {acc_test:.3f}, "
          f"decision crossing P=1/2 at h = {h_cross:.4f}  (true h_c=1)")

    return {
        "h_train": h_train, "y_train": y_train,
        "h_sweep": H_SWEEP, "prob_sweep": prob_sweep,
        "P_train": P_train, "P_sweep": P_sweep,
        "S_sweep": S_sweep, "ZZ_sweep": ZZ_sweep,
        "acc_test": acc_test, "acc_train": acc_train, "h_cross": h_cross,
        "acc_bare": acc_bare, "acc_bare_sd": acc_bare_sd,
        "acc_scram": acc_scram, "acc_scram_sd": acc_scram_sd,
        "S_spt": S_spt, "S_triv": S_triv, "bare_mag": bare_mag,
        "evr": pca.explained_variance_ratio_, "n_shots": N_SHOTS,
        "N": N, "t_ev": T_EV,
    }


data = load_or_compute(DATA_DIR / "fig_ch7_qelm_spt.npz", compute)

h_train, y_train = data["h_train"], data["y_train"]
h_sweep, prob_sweep = data["h_sweep"], data["prob_sweep"]
P_train, P_sweep = data["P_train"], data["P_sweep"]
S_sweep, ZZ_sweep = data["S_sweep"], data["ZZ_sweep"]
acc_test, h_cross = float(data["acc_test"]), float(data["h_cross"])
acc_bare, acc_scram = float(data["acc_bare"]), float(data["acc_scram"])
S_spt, S_triv = float(data["S_spt"]), float(data["S_triv"])
evr = data["evr"]

