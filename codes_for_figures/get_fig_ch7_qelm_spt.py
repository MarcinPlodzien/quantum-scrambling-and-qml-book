#!/usr/bin/env python3
"""
get_fig_ch7_qelm_spt.py
=======================
Classifying a TOPOLOGICAL (SPT) phase with a QUANTUM EXTREME LEARNING MACHINE.

Companion to get_fig_ch7_qelm_phase.py (the symmetry-breaking TFIM example).  The
point here is sharper: the phase carries NO local order parameter, so a linear
readout on the BARE low-weight observables cannot see it, yet the SAME readout on
the SCRAMBLED features succeeds.  Scrambling is what makes the topological order
locally measurable.

Produces Fig. (ch7) -- figures/ch7/fig_ch7_qelm_spt.pdf (+png).

    Panel (a): a 2D PCA of the SCRAMBLED feature vectors, coloured by the field h.
               The ground states trace a smooth arc; the cluster (SPT) and trivial
               training states sit in distinct regions and the critical point
               h_c = 1 is marked on the manifold.
    Panel (b): the trained readout's predicted trivial-phase probability
               P(trivial | h) across the sweep.  The decision crossing lands near
               h_c = 1 even though the readout trained only far from criticality;
               the held-out accuracy is annotated in-panel.
    Panel (c): the physics behind the ablation.  The nonlocal string order S(h)
               drops sharply across h_c while a representative LOCAL order
               parameter <Z_i Z_{i+1}> stays identically zero in both phases.  The
               inset bars report the two classifier accuracies: a linear readout on
               the BARE features is at chance, on the SCRAMBLED features it is high.

MODEL.  Cluster-Ising chain on an OPEN chain of N spins,
    H(h) = -J sum_i Z_{i-1} X_i Z_{i+1}  -  h sum_i X_i,
with a quantum phase transition at h/J = 1 between a symmetry-protected
TOPOLOGICAL (cluster / SPT) phase for h<1 and a trivial paramagnet for h>1.  The
protecting symmetry is Z2 x Z2, generated by the products of X on the even and the
odd sublattices.  The finite open chain has edge modes, so the cluster-phase ground
state is (near-)degenerate; we fix a unique, symmetric ground state by projecting
onto the (+1, +1) symmetry sector before diagonalizing.

WHY LOCAL OBSERVABLES FAIL.  The SPT phase breaks no symmetry, so every local order
parameter forbidden by the Z2 x Z2 symmetry (the magnetizations Z_i, Y_i and the
short correlators Z_iZ_{i+1}, Y_iY_{i+1}) vanishes IDENTICALLY in both phases.  The
order lives instead in the nonlocal string
    S = <Z_1 Y_2 X_3 ... X_{k-2} Y_{k-1} Z_k>
(the product of cluster stabilizers), which is O(1) in the SPT phase and ~0 in the
trivial phase.  A linear readout on the bare symmetry-forbidden bank therefore has
no signal and sits at chance.

WHY SCRAMBLING FIXES IT.  The reservoir is the nonintegrable MIXED-FIELD Ising
chain H_res = J_r sum Z_iZ_{i+1} + h_x sum X_i + h_z sum Z_i.  Its longitudinal
field h_z breaks BOTH the Jordan-Wigner integrability AND the cluster Z2 x Z2
symmetry.  Under U = exp(-i H_res t) a low-weight forbidden O becomes the high-weight
U^dag O U, which is no longer symmetry-forced to vanish and overlaps the string
order.  The same low-weight readout, now on the scrambled features, inherits the
nonlocal order and separates the phases.  (A symmetry-RESPECTING scrambler, however
strong, leaves the forbidden features exactly zero: it is the symmetry breaking,
not scrambling alone, that unlocks the order.)

Cached in data/ch7/fig_ch7_qelm_spt.npz.
"""
